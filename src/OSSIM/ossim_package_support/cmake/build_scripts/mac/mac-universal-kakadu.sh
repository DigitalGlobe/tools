#!/bin/bash
cmake "$@"  \
 -DCMAKE_BUILD_TYPE=Release \
 -DCMAKE_OSX_ARCHITECTURES="i386;x86_64" \
 -DCMAKE_LIBRARY_PATH=$OSSIM_DEPENDENCIES/lib \
 -DCMAKE_INCLUDE_PATH=$OSSIM_DEPENDENCIES/include \
 -DCMAKE_FRAMEWORK_PATH=$OSSIM_DEPENDENCIES/Frameworks \
 -DCMAKE_INSTALL_PREFIX=$OSSIM_INSTALL_PREFIX \
 -DCMAKE_PREFIX_PATH=$OSSIM_DEPENDENCIES \
 -DBUILD_OSSIMPREDATOR=ON\
 -DBUILD_OSSIM_PLUGIN=ON\
 -DBUILD_OSSIMCSM_PLUGIN=ON\
 -DBUILD_OSSIMGDAL_PLUGIN=ON\
 -DBUILD_OSSIMKAKADU_PLUGIN=ON\
 -DBUILD_OSSIMPNG_PLUGIN=ON\
 -DBUILD_OSSIMREGISTRATION_PLUGIN=ON\
 -DBUILD_OSSIMOPENJPEG_PLUGIN=OFF\
 -DBUILD_OSSIM_FRAMEWORKS=ON\
 -DBUILD_SHARED_LIBS=ON \
 -DBUILD_OSSIM_PACKAGES=ON \
 -DOSSIM_BUILD_DOXYGEN=OFF \
 -DBUILD_OSSIM_TEST_APPS=ON \
 -DKAKADU_ROOT_SRC=$OSSIM_DEPENDENCIES/kakadu/include\
 -DKAKADU_LIBRARY=$OSSIM_DEPENDENCIES/kakadu/lib/libkdu_v64R.dylib\
 -DKAKADU_AUX_LIBRARY=$OSSIM_DEPENDENCIES/kakadu/lib/libkdu_a64R.dylib
